class SentenceScaffolder:
    """Provides syntactic scaffolding for sentence generation.

    Given a semantic intent and available concepts, selects appropriate
    sentence patterns and fills them.
    """

    def __init__(self, mapper: ConceptWordMapper):
        self.mapper = mapper

        # Sentence patterns by intent
        self._patterns: Dict[str, List[str]] = {
            "prediction": [
                "The {adj} {noun} will {verb} because of {cause}.",
                "When released, the {adj} {noun} {verb_phrase}.",
                "Due to {cause}, the {noun} {verb_phrase}.",
            ],
            "explanation": [
                "{concept} is {definition}.",
                "{concept} refers to {definition}.",
                "In physics, {concept} means {definition}.",
            ],
            "description": [
                "The {adj1} {adj2} {noun} is {property}.",
                "This is a {adj} {noun} made of {material}.",
                "I see a {adj} {noun} with {property}.",
            ],
            "counterfactual": [
                "If {condition}, then {prediction}.",
                "In a scenario where {condition}, {prediction}.",
                "Suppose {condition}. Then {prediction}.",
            ],
            "action": [
                "I will {verb} the {noun}.",
                "To {goal}, I need to {verb} the {noun}.",
                "Plan: {steps}.",
            ],
            "code": [
                "Here's a {language} function that {description}:\n\n```{language}\n{code}\n```",
                "I can write that in {language}:\n\n```{language}\n{code}\n```",
            ],
            "abstract": [
                "{concept} implies {implication}.",
                "From {concept}, we can derive {implication}.",
                "The principle of {concept} leads to {implication}.",
            ],
        }

    def get_pattern(self, intent: str) -> str:
        """Get a sentence pattern for an intent."""
        patterns = self._patterns.get(intent, ["{text}"])
        return patterns[0]  # Simple selection; could be smarter

    def fill_pattern(self, pattern: str, slots: Dict[str, str]) -> str:
        """Fill a pattern with slot values."""
        result = pattern
        for key, value in slots.items():
            result = result.replace(f"{{{key}}}", str(value))
        # Clean up unfilled slots
        result = re.sub(r'\{[a-z_]+\}', '', result)
        result = re.sub(r'\s+', ' ', result).strip()
        return result


