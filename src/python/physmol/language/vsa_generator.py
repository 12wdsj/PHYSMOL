    def _match_code_patterns(self, task: str, language: str
                              ) -> List[Tuple[CodePattern, float]]:
        """Match a task description to code patterns."""
        task_lower = task.lower()
        matches = []

        for pattern in self._code_patterns.values():
            if pattern.language != language:
                continue

            # Score based on keyword overlap, with bonus for multi-word matches
            score = 0.0
            matched_keywords = 0
            matched_kw_list = []
            for kw in pattern.keywords:
                if kw in task_lower:
                    # Multi-word keywords get higher weight
                    weight = len(kw.split())
                    score += weight
                    matched_keywords += 1
                    matched_kw_list.append(kw)

            if matched_keywords > 0:
                # Normalize by number of keywords, but reward more matches
                score = score / len(pattern.keywords) * (1 + matched_keywords * 0.2)

                # Bonus for matching the primary action (sort > list, search > array)
                primary_actions = ["sort", "search", "traverse", "push", "pop",
                                   "enqueue", "dequeue", "bfs", "dfs"]
                for action in primary_actions:
                    if action in matched_kw_list:
                        score *= 1.5
                        break

                matches.append((pattern, score))

        matches.sort(key=lambda x: x[1], reverse=True)
        return matches

    def _extract_code_params(self, task: str, pattern: CodePattern
                              ) -> Dict[str, str]:
        """Extract parameters from task description for code template."""
        params: Dict[str, str] = {}
        task_lower = task.lower()

        # Extract function/class name
        name_found = False
        name_match = re.search(
            r'(?:called|named)\s+(\w+)', task, re.IGNORECASE)
        if name_match:
            params["name"] = name_match.group(1)
            name_found = True

        if not name_found:
            # Try to find a meaningful name from the task
            # Look for patterns like "binary search function", "linked list class"
            name_patterns = [
                r'(binary\s+search)',
                r'(linked\s+list)',
                r'(graph\s+(?:bfs|dfs|traversal))',
                r'(quicksort|mergesort|heap\s*sort)',
            ]
            for pat in name_patterns:
                m = re.search(pat, task, re.IGNORECASE)
                if m:
                    params["name"] = m.group(1).replace(" ", "_").lower()
                    name_found = True
                    break

        if not name_found:
            # Fallback: extract last meaningful word before "function"/"class"
            name_match = re.search(
                r'(?:function|def|class|implement|write)\s+(?:a\s+)?(?:\w+\s+)*?(\w+)',
                task, re.IGNORECASE)
            if name_match:
                candidate = name_match.group(1).lower()
                # Filter out common words
                if candidate not in {"a", "an", "the", "that", "for", "to", "with",
                                      "function", "class", "code", "program", "write",
                                      "implement", "create", "make"}:
                    params["name"] = candidate
                    name_found = True

        if not name_found and "name" in pattern.template:
            # Generate a name from the task
            words = re.findall(r'[a-z]+', task_lower)
            # Filter out common stop words
            stop_words = {"a", "an", "the", "to", "for", "in", "of", "and", "or", "is",
                          "that", "this", "with", "from", "it", "write", "create", "make",
                          "define", "implement", "build"}
            meaningful = [w for w in words if w not in stop_words][:3]
            params["name"] = "_".join(meaningful) if meaningful else "my_function"

        # Extract parameters
        param_match = re.search(
            r'(?:parameters?|arguments?|params?|takes?|accepts?)\s+(.+?)(?:\.|$)',
            task, re.IGNORECASE)
        if param_match:
            params["params"] = param_match.group(1).strip()
        elif "params" in pattern.template:
            params["params"] = ""

        # Extract description/docstring
        params["docstring"] = task[:100]
        params["description"] = task

        # Extract language
        params["language"] = pattern.language

        return params

