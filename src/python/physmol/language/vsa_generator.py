    def _resolve_subject(self, subject: str) -> str:
        """Resolve a subject description to natural language."""
        # Try to find in recipe store
        recipe = self.recipe_store.get_recipe(subject)
        if recipe:
            return self.generate_description(subject)

        # Check if it matches any known word
        entry = self.mapper.find_word(subject)
        if entry:
            return entry.text

        return subject

    def _generate_from_vsa(self, concept: str) -> str:
        """Generate description by decomposing a VSA concept."""
        # Try to decompose as an attribute ID
        cat, name = self.primitives.resolve_id(concept)
        if cat:
            entry = self.mapper.concept_to_word(cat, name)
            if entry:
                return f"{concept} is a {cat} attribute meaning '{entry.text}'."

        return f"I have a concept '{concept}' in my VSA space but need more experience to describe it."

    def _gen_question(self, parsed: dict, result: dict) -> str:
        """Generate a question response."""
        if "prediction" in result:
            details = []
            if result.get("matched_objects"):
                for obj_id, score in result["matched_objects"][:3]:
                    desc = self.generate_description(obj_id)
                    details.append(f"{obj_id}: {desc} (similarity: {score:.2f})")
            if result.get("applicable_rules"):
                details.extend(result["applicable_rules"])

            # Extract subject from parsed query's matching objects (keyword-based)
            subject = "the object"
            if parsed.get("matching_objects"):
                best_obj = parsed["matching_objects"][0][0]
                desc = self.generate_description(best_obj)
                subject = desc
            elif result.get("matched_objects"):
                best_obj = result["matched_objects"][0][0]
                desc = self.generate_description(best_obj)
                subject = desc

            # Extract cause from applicable rules
            cause = "physics"
            if result.get("applicable_rules"):
                cause = result["applicable_rules"][0]

            return self.generate_prediction(
                subject=subject,
                prediction=result["prediction"],
                cause=cause,
                details=details if details else None,
            )

        if "matched_objects" in result:
            matches = result["matched_objects"]
            if matches:
                lines = []
                for obj_id, score in matches:
                    desc = self.generate_description(obj_id)
                    lines.append(f"  - {obj_id}: {desc} (score: {score:.2f})")
                return f"I found {len(matches)} object(s) matching your description:\n" + "\n".join(lines)

        # Check if the result is an explanation (from concept matching)
        if result.get("explanation") and result.get("concept"):
            return self._gen_explanation(parsed, result)

        return f"I couldn't find objects matching '{parsed.get('text', '')}' in my knowledge base. I may need to explore more."

    def _gen_command(self, parsed: dict, result: dict) -> str:
        """Generate a command response."""
        if "plan" in result:
            return self.generate_action_plan(
                action=result.get("action", "execute"),
                target=result.get("target", "object"),
                steps=result["plan"],
            )
        return f"I understand you want to {parsed.get('text', '')}, but I cannot execute this command."

    def _gen_explanation(self, parsed: dict, result: dict) -> str:
        """Generate an explanation response."""
        concept = result.get("concept", parsed.get("text", ""))

        # Check if this is a code explanation from reasoning engine
        if result.get("kind") == "code_explanation":
            return self._gen_code_explanation(result)

        # Use explanation data from the result if available (e.g., from learned concepts)
        explanation = result.get("explanation", {})
        if explanation.get("definition"):
            response = f"**{concept}**\n\n"
            response += f"Definition: {explanation['definition']}\n\n"
            if explanation.get("physics"):
                response += f"Physics: {explanation['physics']}\n\n"
            if explanation.get("examples"):
                response += "Examples:\n"
                for ex in explanation["examples"]:
                    response += f"  - {ex}\n"
            return response

        related = result.get("related_objects")
        return self.generate_explanation(concept, related)

    def _gen_code_explanation(self, result: dict) -> str:
        """Generate explanation for code/algorithm concepts."""
        concept = result.get("concept", "unknown")
        explanation = result.get("explanation", {})

        response = f"**{concept}**\n\n"
        response += f"Definition: {explanation.get('definition', '')}\n\n"

        if explanation.get("algorithm"):
            response += f"Algorithm: {explanation['algorithm']}\n\n"

        complexity = explanation.get("complexity", {})
        if complexity:
            response += "Complexity:\n"
            for k, v in complexity.items():
                response += f"  - {k}: {v}\n"
            response += "\n"

        use_cases = explanation.get("use_cases", [])
        if use_cases:
            response += "Use cases:\n"
            for uc in use_cases:
                response += f"  - {uc}\n"

        return response

    def _gen_algorithm_comparison(self, result: dict) -> str:
        """Generate a comparison between two algorithms."""
        algorithms = result.get("algorithms", [])
        if len(algorithms) < 2:
            return "I need two algorithms to compare."

        algo1, algo2 = algorithms[0], algorithms[1]
        response = f"Comparing **{algo1}** vs **{algo2}**:\n\n"

        # Show each algorithm's properties
        for algo in [algo1, algo2]:
            algo_info = result.get(algo, {})
            response += f"**{algo}:**\n"
            if algo_info.get("definition"):
                response += f"  {algo_info['definition']}\n"
            complexity = algo_info.get("complexity", {})
            if complexity:
                complexity_str = ", ".join(f"{k}: {v}" for k, v in complexity.items())
                response += f"  Complexity: {complexity_str}\n"
            response += "\n"

        # Show comparison points
        comparison_points = result.get("comparison_points", [])
        if comparison_points:
            response += "Key differences:\n"
            for point in comparison_points:
                aspect = point.get("aspect", "")
                response += f"  - {aspect}:\n"
                response += f"    {algo1}: {point.get(algo1, 'N/A')}\n"
                response += f"    {algo2}: {point.get(algo2, 'N/A')}\n"

        # Show shared concepts
        shared = result.get("shared_concepts", [])
        if shared:
            response += f"\nShared concepts: {', '.join(shared)}"

        return response

    def _gen_code_with_reasoning(self, parsed: dict, result: dict) -> str:
        """Generate code with reasoning context from VSA."""
        task = result.get("task", parsed.get("text", ""))
        reasoning = result.get("reasoning", {})

