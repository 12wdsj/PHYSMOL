# ---------------------------------------------------------------------------
# Word and Phrase representations
# ---------------------------------------------------------------------------

@dataclass
class WordEntry:
    """A word in the generator's vocabulary."""
    text: str
    pos: str  # part of speech: noun, verb, adj, adv, prep, det, conj
    category: str = ""  # VSA category (shape, color, material, ...)
    tags: List[str] = field(default_factory=list)  # semantic tags


@dataclass
class PhraseTemplate:
    """A phrase template with slots to fill."""
    pattern: str  # e.g., "{adj} {noun}", "the {noun} {verb}"
    pos_sequence: str  # e.g., "ADJ NOUN", "DET NOUN VERB"
    description: str = ""


@dataclass
class CodePattern:
    """A code generation pattern."""
    name: str
    language: str  # python, javascript, c, etc.
    description: str
    template: str  # code template with {slots}
    keywords: List[str] = field(default_factory=list)
    input_types: List[str] = field(default_factory=list)
    output_type: str = ""


