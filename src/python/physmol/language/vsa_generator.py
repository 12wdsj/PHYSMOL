    def generate_description(self, object_id: str) -> str:
        """Generate a natural language description of an object from its recipe."""
        recipe = self.recipe_store.get_recipe(object_id)
        if recipe is None:
            return f"I don't know about '{object_id}'."

        entries = self.mapper.describe_attributes(recipe)
        if not entries:
            return f"I recognize '{object_id}' but can't describe it yet."

        # Group by part of speech
        adjs = [e.text for e in entries if e.pos == "adj"]
        nouns = [e.text for e in entries if e.pos == "noun"]

        noun_phrase = nouns[0] if nouns else "object"
        if adjs:
            adj_str = ", ".join(adjs[:-1]) + " and " + adjs[-1] if len(adjs) > 1 else adjs[0]
            return f"a {adj_str} {noun_phrase}"
        return f"a {noun_phrase}"

    def generate_prediction(self, subject: str, prediction: str,
                            cause: str = "", details: Optional[List[str]] = None) -> str:
        """Generate a prediction response from VSA concepts."""
        desc = self._resolve_subject(subject)
        cause_text = cause or "physics"

        # If the prediction text is already a full, meaningful sentence, use it directly
        # Only wrap in template if it's a short fragment
        if prediction and len(prediction) > 60:
            result = prediction
        else:
            # Determine the right verb based on cause/prediction context
            lower_pred = prediction.lower() if prediction else ""
            lower_cause = cause_text.lower()
            if "fall" in lower_pred or "drop" in lower_pred or "gravity" in lower_cause:
                verb = "fall downward"
            elif "slide" in lower_pred or "friction" in lower_cause:
                verb = "decelerate and stop"
            elif "bounce" in lower_pred or "elastic" in lower_cause:
                verb = "bounce back"
            elif "collide" in lower_pred or "momentum" in lower_cause or "collision" in lower_cause:
                verb = "transfer momentum"
            elif "roll" in lower_pred:
                verb = "roll along the surface"
            else:
                verb = prediction if prediction else "respond according to its physical properties"

            pattern = self.scaffolder.get_pattern("prediction")
            result = self.scaffolder.fill_pattern(pattern, {
                "adj": "",
                "noun": desc,
                "verb": verb,
                "cause": cause_text,
                "verb_phrase": prediction if prediction else verb,
            })

        if details:
            result += "\n\nDetails:\n" + "\n".join(f"  - {d}" for d in details)
        return result

    def generate_explanation(self, concept: str, related_objects: Optional[List[Tuple[str, float]]] = None) -> str:
        """Generate a concept explanation from VSA knowledge."""
        concept_lower = concept.lower()
        defn = self._definitions.get(concept_lower)

        if defn:
            result = f"**{concept}**\n\n"
            result += f"Definition: {defn['definition']}\n\n"
            result += f"Physics: {defn['physics']}\n\n"
            if defn.get("examples"):
                result += "Examples:\n"
                for ex in defn["examples"]:
                    result += f"  - {ex}\n"
        else:
            # Try to generate from VSA decomposition
            result = f"**{concept}**\n\n"
            result += self._generate_from_vsa(concept)

        if related_objects:
            result += "\nRelated objects in my experience:\n"
            for obj_id, score in related_objects[:5]:
                desc = self.generate_description(obj_id)
                result += f"  - {obj_id}: {desc} (similarity: {score:.2f})\n"

        return result

    def generate_counterfactual(self, subject: str, change: str,
                                 reasoning: str, prediction: str) -> str:
        """Generate a counterfactual reasoning response."""
        pattern = self.scaffolder.get_pattern("counterfactual")
        condition = f"the {subject} were {change}"
        return self.scaffolder.fill_pattern(pattern, {
            "condition": condition,
            "prediction": prediction,
        })

    def generate_action_plan(self, action: str, target: str,
                              steps: List[str]) -> str:
        """Generate an action plan response."""
        result = f"I will {action} the {target}.\n\nPlan:\n"
        for i, step in enumerate(steps, 1):
            result += f"  {i}. {step}\n"
        return result

    def generate_code(self, task: str, language: str = "python",
                      context: Optional[Dict[str, Any]] = None) -> str:
        """Generate code from a natural language task description.

        Uses VSA pattern matching to find the best code template,
        then fills in the slots based on the task description.
        """
        # Find matching code patterns
        matches = self._match_code_patterns(task, language)
        if not matches:
            return self._generate_code_fallback(task, language)

        best_pattern, score = matches[0]

        # Extract parameters from task description
        params = self._extract_code_params(task, best_pattern)

        # Fill the template
        code = best_pattern.template
        for key, value in params.items():
            code = code.replace(f"{{{key}}}", str(value))

        # Clean up unfilled slots with reasonable defaults
        code = self._fill_code_defaults(code, best_pattern, params)

        return code

    def generate_from_reasoning(self, parsed_query: dict,
                                 reasoning_result: dict) -> str:
        """Main entry point: generate response from reasoning results.

        This replaces the template-based Responder.respond().
        """
        intent = parsed_query.get("intent", "unknown")
        kind = reasoning_result.get("kind")

        # Route to specific generators based on result kind
        if kind == "abstract_reasoning":
            return self._gen_abstract(reasoning_result)
        if kind == "theory_of_mind":
            return self._gen_social(reasoning_result)
        if kind == "conversation":
            return reasoning_result.get("response", "")
        if kind == "memory":
            return self._gen_memory(reasoning_result)
        if kind == "cross_domain_transfer":
            return self._gen_transfer(reasoning_result)
        if kind == "abstract_task":
            return self._gen_abstract_task(reasoning_result)
        if kind == "code_explanation":
            return self._gen_code_explanation(reasoning_result)
        if kind == "algorithm_comparison":
            return self._gen_algorithm_comparison(reasoning_result)
        if kind == "code_reasoning":
            return self._gen_code_with_reasoning(parsed_query, reasoning_result)

        # Route based on intent
        if intent == "question":
            return self._gen_question(parsed_query, reasoning_result)
        elif intent == "command":
            return self._gen_command(parsed_query, reasoning_result)
        elif intent == "explanation":
            return self._gen_explanation(parsed_query, reasoning_result)
        elif intent == "counterfactual":
            return self._gen_counterfactual(parsed_query, reasoning_result)
        elif intent == "code":
            return self.generate_code(
                parsed_query.get("text", ""),
                context=reasoning_result,
            )
        else:
            return self._gen_unknown(parsed_query)

